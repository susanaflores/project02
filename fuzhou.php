    <h2>Cuisine</h2>
    <h3>Fo Tiao Qiang</h3>
    <p>
      One of the most renowned Fuzhou dishes is Fotiaoqiang (Buddha Jumps Over the wall). It is said to have been created by the wife of a Fuzhou official, who in 1876 was entertaining an important member of the Fujian Government in his home. His wife, who was an excellent cook placed chicken, duck, pork and seafood together into an empty wine jar to simmer following a recipe which was known at the time as 'Full of Blessing and Longevity'. When the dish was served, the visitor praised the aroma and the delicious flavor and was so impressed that when he returned home he asked the cook – Zheng Chunfa to learn how the wife of his host had prepared the food.
    </p>

    <p>
      Subsequently, Zheng Chunfa adapted the recipe making it yet more fulsome and on the strength of his creation opened a restaurant named Juchunyuan where he served it to the delight of his customers. One day several talented writers to the restaurant and were served with Zheng Chunfa's special version of 'Full of Blessing and Longevity'. When he opened the jar in which it had been cooked, the diners were so taken with the gorgeous smell that one of them wrote a poem about it. In it he said that the smell was so wonderful that even a Buddha would stop pattering and jump over the wall to have a taste. Hence the name 'Buddha Jumps over the Wall'.
    </p>
