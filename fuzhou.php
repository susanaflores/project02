    <h2>Climate</h2>
    <p>
        Fuzhou has a humid subtropical climate (Köppen Cfa) influenced by the East Asian Monsoon; the summers are long, very hot and humid, and the winters are short, mild and dry. In most years, torrential rain occurs during the monsoon in the second half of May. Fuzhou is also liable to typhoons in late summer and early autumn. The monthly 24-hour average temperature ranges from 10.9 °C (51.6 °F) in January to 28.9 °C (84.0 °F) in July, while the annual mean is 19.84 °C (67.7 °F).
      </p>

      <p>
        With monthly percent possible sunshine ranging from 24 percent in March to 54 percent in July, the city receives 1,607 hours of bright sunshine annually. Extremes since 1951 have ranged from −1.9 °C (29 °F) on 25 January 2016 to 41.7 °C (107 °F) on 26 July 2003. Snow is very rare, having covered the ground last times in February 1957, December 1975 and December 1991.
      </p>
