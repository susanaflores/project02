      <h2>Cuisine</h2>
      <h3>Mission-Style Burritos</h3>

      <p>
        The Mission-style burrito — or San Francisco burrito — was created in the 1960s in The City’s Mission District. There are two theories as to when and where the first one was served: September 26, 1961 at El Faro, or at September 29, 1969 at Taqueria La Cumbre. Regardless of who was its originator, the Mission-style burrito has become an indispensable native food for San Franciscans.
      </p>

      <p>
        What sets it apart from the original meat, rice, and beans variety, is that this monster gets super-sized with the addition of extra rice, vegetables, sour cream, and cheese (in a super burrito), and wrapped in a steamed tortilla to keep all the goodness in. Besides El Faro and La Cumbre, another popular burrito institution one should visit to get the traditional Mission style burrito experience is La Taqueria.
      </p>
