      <h2>History</h2>
      <p>
          The earliest archaeological evidence of human habitation of the territory of the city of San Francisco dates to 3000 BC. The Yelamu group of the Ohlone people resided in a few small villages when an overland Spanish exploration party, led by Don Gaspar de Portolá, arrived on November 2, 1769, the first documented European visit to San Francisco Bay.
        </p>
        <p>
          The first maritime presence occurred on August 5, 1775, when San Carlos - commanded by Juan Manuel de Ayala - became the first ship to anchor in the bay. The following year, on March 28, 1776, the Spanish established the Presidio of San Francisco, followed by a mission, Mission San Francisco de Así­s (Mission Dolores), established by the Spanish explorer Juan Bautista de Anza.
        </p>
