    <h2>Climate</h2>
    <p>
        Yangzhou has a subtropical monsoon climate with humid changeable wind; longer winters for about 4 months, summers 3 months and shorter springs and autumns, 2 months respectively; frost-free period of 222 days and annual average sunshine of 2177 hours. The mean annual temperature is 15.72 °C (60.3 °F) annually; the normal monthly mean 24-hour temperature ranges from 2.5 °C (36.5 °F) in January to 28.0 °C (82.4 °F) in July.
      </p>

      <p>
        The annual average precipitation is 1,043 mm (41.1 in), and about 45 percent of rainfall is concentrated in the summer. The rainy season known as "plum rain season" usually lasts from mid-June to late July. During this season, the plums are ripening, hence the name plum rain.
