     <p>
      Yangzhou is located on a plain north of the Yangtze. The Grand Canal, also known as the Jing-Hang Canal, crosses the prefecture-level from the north to the south; its modern route passes through the eastern outskirts of Yangzhou's main urban area, while its old route runs through the city center. Like much of the entire prefecture-level city, Yangzhou's main urban area (the "city proper") is criss-crossed by an intricate network of canals and small lakes. Other major bodies of water within the prefecture-level city include the Baoshe River, Datong River, Beichengzi River, Tongyang Canal, Xintongyang Canal, Baima Lake, Baoying Lake, Gaoyou Lake and Shaobo Lake.
